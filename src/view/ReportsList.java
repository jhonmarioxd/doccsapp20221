    private void ReportsDataBtnActionPerformed(java.awt.event.ActionEvent evt) {                                               
        ReportsDAOImp reportsDAOImp = new ReportsDAOImp();
        //Se traen los datos de la BD
        try {
            listReports = (ArrayList<Reports>) reportsDAOImp.listReports();
            Object[] data = new Object[dtmReports.getColumnCount()];
            int i = 1;
            dtmReports.setRowCount(0);
            for (Reports reports : listReports) {
                data[0] = reports.getTitlePage();
                data[1] = reports.getRating();
                data[2] = reports.getIntroduction();
                data[3] = reports.getReportBody();
                data[4] = reports.getConclusions();
                data[5] = reports.getBibliography();
                i++;
                dtmReports.addRow(data);
            }
            ReportsTbl.setModel(dtmReports);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }                                              

