    public boolean Verificar_Emails(String Correo) {
        Pattern patron = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)+*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");

        Matcher mat = patron.matcher(Correo);

        if (mat.find()) {

            return true;
        } else {
            return false;
        }
    }

