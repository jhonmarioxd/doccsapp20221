    private void ReportsDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        int index = ReportsTbl.getSelectedRow();
        Reports reports = listReports.get(index);
        //JOptionPane.showMessageDialog(null, client.getId());
        ReportsDAOImp reportsDAOImp = new ReportsDAOImp();
        int rta = JOptionPane.showConfirmDialog(null, "Do you want to delete the record?",
            "Remove reports Registry", JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.INFORMATION_MESSAGE);
        if (rta == 0) {
            try {
                reportsDAOImp.deleteReports(reports);
                JOptionPane.showMessageDialog(null, "Deleted Reports Record");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Operation Not Performed");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operation Not Performed");
        }
        if (dtmReports.getRowCount() > 0) {
            for (int i = dtmReports.getRowCount() - 1; i > -1; i--) {
                dtmReports.removeRow(i);
            }
            try {
                var Reports = reportsDAOImp.listReports();
                Object[] data = new Object[dtmReports.getColumnCount()];
                int i = 1;
                dtmReports.setRowCount(0);
                for (Reports c : Reports) {
                    data[0] = c.getTitlePage();
                    data[1] = c.getRating();
                    data[2] = c.getIntroduction();
                    data[3] = c.getReportBody();
                    data[4] = c.getConclusions();
                    data[5] = c.getBibliography();
                    i++;
                    dtmReports.addRow(data);
                }
                ReportsTbl.setModel(dtmReports);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }                                                

